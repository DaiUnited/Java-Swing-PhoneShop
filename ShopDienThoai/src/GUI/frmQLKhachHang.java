    public void fillDataToTable() {
    KhachHangDao khDao = new KhachHangDao();
    listKH = khDao.XemThongTin();
    tblModel = (DefaultTableModel) tblKhachHang.getModel();
    tblModel.setRowCount(0);
    for (KhachHang kh : listKH) {
        Object[] row = new Object[]{
            kh.getMaKH(),
            kh.getHoKH(),
            kh.getTenKH(),
            kh.GioiTinh(),
            kh.getCMND(),
            kh.getDienThoai(),
            kh.getDiaChi(),
        };
        tblModel.addRow(row);
    }
    tblKhachHang.setModel(tblModel);
}
    public void showForm() {
    index = tblKhachHang.getSelectedRow();
    if (index < 0) {
        JOptionPane.showMessageDialog(this, "Bạn chưa chọn dòng trên bảng");
        return;
    } else {
        KhachHang kh = listKH.get(index);
        txtMaKH.setText(kh.getMaKH());
        txtHoKH.setText(kh.getHoKH());
        txtTenKH.setText(kh.getTenKH());
        txtCMND.setText(kh.getCMND());
        txtDienThoai.setText(kh.getDienThoai());
        if (kh.getGioiTinh() == 1) {
            rd_Nam.setSelected(true);
        } else {
            rd_Nu.setSelected(true);
        }
        txtDiaChi.setText(kh.getDiaChi());
        txtGhiChu.setText(kh.getGhiChu());
        setEditable(true);
    }
}
