    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        String oldPassword = String.valueOf(txtMKCu.getPassword());
        String newPassword = String.valueOf(txtMKMoi.getPassword());
        String confirmPassword = String.valueOf(txtMKXacNhan.getPassword());
        if (newPassword.equals(confirmPassword)) {
            Account currentAccount = LoginDao.getCurrentAccount();
            if (currentAccount != null) {
                try {
                    changePassword(currentAccount.getUsername(), oldPassword, newPassword);
                } catch (Exception ex) {
                    Logger.getLogger(frmDoiMatKhau.class.getName()).log(Level.SEVERE, null, ex);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Lỗi: Không tìm thấy thông tin tài khoản hiện tại.");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Mật khẩu mới và xác nhận mật khẩu không khớp!");
        }
    }//GEN-LAST:event_btnLuuActionPerformed

