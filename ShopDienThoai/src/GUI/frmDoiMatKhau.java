    private void changePassword(String username, String oldPassword, String newPassword) throws Exception {
        LoginDao dao = new LoginDao();
        boolean isChanged = dao.changePassword(username, oldPassword, newPassword);

        if (isChanged) {
            JOptionPane.showMessageDialog(this, "Đổi mật khẩu thành công!");
        } else {
            JOptionPane.showMessageDialog(this, "Đổi mật khẩu thất bại. Vui lòng kiểm tra lại mật khẩu cũ.");
        }
    }
   

